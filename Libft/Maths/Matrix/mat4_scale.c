#include "../../libft.h"

t_Mat4	mat4_scalef(float value)
{
	return (mat4(value));
}

t_Mat4	mat4_scalev3(t_Vec3 vec)
{
	t_Mat4	mat;

	mat = mat4_identity();
	mat.m[0][0] = vec.x;
	mat.m[1][1] = vec.y;
	mat.m[2][2] = vec.z;
	return (mat);
}
